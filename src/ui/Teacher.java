    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        try {

            Connection con = Db.createConnetion();
            String sql = "INSERT INTO teacher( name,address, age, gender, contact) VALUES (?,?,?,?,?)";
            PreparedStatement stm = con.prepareStatement(sql);
            stm.setString(1, txt_name.getText());
            stm.setString(2, txt_address.getText());
            stm.setInt(3, Integer.parseInt(txt_age.getText()));
            stm.setString(4, (String) gender.getSelectedItem());
            stm.setInt(5, Integer.parseInt(txtContact.getText()));
            int response = stm.executeUpdate();
            if (response > 0) {
                //dispose();
                JOptionPane.showMessageDialog(this, "Teacher Add Succesfully");
                show_teachers();
            }
        } catch (Exception ex) {
            Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btn_saveActionPerformed

