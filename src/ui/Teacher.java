    private void show_teachers() {

        ResultSet r = null;
        try {
            Connection con = Db.createConnetion();
            Statement stmt = con.createStatement();
            String sql = "SELECT Id, name, address, age, gender,contact FROM teacher";
            r = stmt.executeQuery(sql);

            model = (DefaultTableModel) Teacher_Table.getModel();

            model.setRowCount(0);

            while (r.next()) {
                Vector v = new Vector();
                v.add(Integer.toString(r.getInt("Id")));
                v.add(r.getString("name"));
                v.add(r.getString("address"));
                v.add(Integer.toString(r.getInt("age")));
                v.add(r.getString("gender"));
                v.add(Integer.toString(r.getInt("contact")));

                model.addRow(v);

            }

            Teacher_Table.setModel(model);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

